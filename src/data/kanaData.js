// Get all kana as flat array
export const getAllKana = () => {
  return [...kanaData.清音, ...kanaData.濁音, ...kanaData.拗音];
};

// Get kana by type
export const getKanaByType = (type) => {
  return kanaData[type] || [];
};

// Get random kana from a specific type or all types
export const getRandomKana = (types = ['清音', '濁音', '拗音']) => {
  const selectedKana = types.reduce((acc, type) => {
    return acc.concat(kanaData[type] || []);
  }, []);
  
  if (selectedKana.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * selectedKana.length);
  return selectedKana[randomIndex];
};

